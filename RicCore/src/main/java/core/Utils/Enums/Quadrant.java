package core.Utils.Enums;

public enum Quadrant {
	/**
	 * <p>firstQuadrant:第一象限，X>0,Y>=0</p>
	 */
	firstQuadrant,
	/**
	 * <p>secondQuadrant:第二象限，X<=0,Y>0</p>
	 */
	secondQuadrant,
	/**
	 * <p>thirdQuadrant:第三象限，X<0,Y<=0</p>
	 */
	thirdQuadrant,
	/**
	 * <p>forthQuadrant:第四象限，包括X>=0，Y<0</p>
	 */
	forthQuadrant
}
